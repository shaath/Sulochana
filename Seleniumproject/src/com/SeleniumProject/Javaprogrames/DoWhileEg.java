package com.SeleniumProject.Javaprogrames;

public class DoWhileEg {

	public static void main(String[] args) 
	{
		int i=1;
		
		do
		{
			System.out.println(i);
			i++;
		}
		while(i<=100);
		
		System.out.println("*******");
		
		int j=100;
		do{
			System.out.println(j);
			j--;
		}while(j>=1);
	}
		

}
